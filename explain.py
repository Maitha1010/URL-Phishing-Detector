"""
explain.py
----------
Explainability layer for the phishing URL detector.

Runs SHAP and LIME on the same sample of URLs using the best model (XGBoost),
computes comparison metrics, and generates two explanation formats per URL:
  - Technical  : ranked feature contributions, model confidence, SHAP vs LIME
  - Plain-language : 2-3 sentence explanation built from validated templates
                     (NOT generated by a language model -- fully traceable)

IMPORTANT (from project spec):
  Plain-language explanations MUST use templates filled with real SHAP signals.
  This guarantees every claim in the explanation is traceable to a measured value.

Run from the project root:
    python src/explain.py

Outputs:
    results/explainability_metrics.csv   -- fidelity, stability, sparsity, consistency, runtime
    results/example_explanations.csv     -- full explanations for 20 example URLs
    photos/fig11_shap_summary.png
    photos/fig12_shap_feature_importance.png
    photos/fig13_lime_example.png
    photos/fig14_shap_lime_consistency.png
    photos/fig15_explainability_metrics.png
"""

